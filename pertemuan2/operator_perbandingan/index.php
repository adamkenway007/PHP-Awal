<?php
// Operator Perbandingan
// <  : Lebih Kecil Dari
// >  : Lebih Besar Dari
// <= : Lebih Kecil Sama Dengan
// >= : Lebih Besar Sama Dengan
// == : Sama Dengan
// != : Tidak Sama Dengan
echo "<h4>------------------------</h4>";
echo "<h4>Lebih Kecil Dari<h4/>";
echo "<h4>var_dump(2 < 5)<h4/>";
var_dump(2 < 5);
echo "<h4>------------------------</h4>";
echo "<br />";
echo "<h4>------------------------</h4>";
echo "<h4>Lebih Besar Dari<h4/>";
echo "<h4>var_dump(2 > 5)<h4/>";
var_dump(2 > 5);
echo "<h4>------------------------</h4>";
echo "<br />";
echo "<h4>------------------------</h4>";
echo "<h4>Lebih Kecil Sama Dengan<h4/>";
echo "<h4>var_dump(2 <= 5);<h4/>";
var_dump(2 <= 5);
echo "<h4>------------------------</h4>";
echo "<br />";
echo "<h4>------------------------</h4>";
echo "<h4>Lebih Besar Sama Dengan<h4/>";
echo "<h4>var_dump(2 >= 5)<h4/>";
var_dump(2 >= 5);
echo "<h4>------------------------</h4>";
echo "<br />";
echo "<h4>------------------------</h4>";
echo "<h4>Sama Dengan<h4/>";
echo "<h4>var_dump(2 == 5)<h4/>";
var_dump(2 == 5);
echo "<h4>------------------------</h4>";
echo "<br />";
echo "<h4>------------------------</h4>";
echo "<h4>Tidak Sama Dengan<h4/>";
echo "<h4>var_dump(2 != 5)<h4/>";
var_dump(2 != 5);
echo "<h4>------------------------</h4>";
?>